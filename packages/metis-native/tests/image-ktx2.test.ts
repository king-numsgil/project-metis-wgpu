let dir = "";

beforeAll(async () => {
    dir = mkdtempSync(join(tmpdir(), "ktx2-"));
    adapter = await requestAdapter();
    if (!adapter) return;
    hasBc = adapter.features.has("texture-compression-bc");
    if (hasBc) {
        device = await adapter.requestDevice({
            label: "ktx2-test",
            requiredFeatures: ["texture-compression-bc"],
        });
    }
    plainDevice = await adapter.requestDevice({ label: "ktx2-test-no-bc" });
});

function write(name: string, bytes: Uint8Array): string {
    const path = join(dir, name);
    writeFileSync(path, bytes);
    return path;
}

/**
 * Loads and fails if wgpu complained.
 *
 * This is the load-bearing part of every success-path test: a wrong
 * `bytes_per_row` produces a valid-looking `GpuTexture` and a validation error
 * that nothing would otherwise observe.
 *
 * **This used to wrap the load in `pushErrorScope`/`popErrorScope`, and that
 * stopped working in the wgpu 30 upgrade.** wgpu 30 made error scopes
 * thread-local, and `loadKtx2Texture` does its GPU work inside an
 * `AsyncTask::compute` on a libuv worker — so a scope opened here, on the JS
 * thread, could not see it. The pop returned `null` unconditionally and every
 * assertion below it was vacuous while staying green.
 *
 * The loader now brackets its own GPU work in a scope on the worker thread and
 * rejects the promise (see `gpu::error::with_validation_scope`), so an
 * unobserved validation error becomes a failed `await` right here. Nothing to
 * remember to wrap; `loadGpuValidationRejects` below pins that it really fires.
 */
async function loadChecked(dev: GpuDevice, path: string) {
    return await loadKtx2Texture(dev, path);
}

describe("loadKtx2Texture", () => {
    it("loads a mipped BC7 texture, reporting format and mip count from the file", async () => {
        if (!device) return;
        const levels = mipChain(64, 64, BC7.blockBytes, BC7.blockW, BC7.blockH);
        const path = write("bc7.ktx2", buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 64, height: 64, levels, ...BC7 }));

        const texture = await loadChecked(device, path);
        expect(texture.width).toBe(64);
        expect(texture.height).toBe(64);
        expect(texture.format).toBe("bc7-rgba-unorm");
        expect(texture.mipLevelCount).toBe(7); // 64,32,16,8,4,2,1
    });

    it("maps the sRGB block format to the -srgb texture format", async () => {
        if (!device) return;
        const levels = mipChain(16, 16, BC7.blockBytes, BC7.blockW, BC7.blockH);
        const path = write(
            "bc7-srgb.ktx2",
            buildKtx2({ vkFormat: VK.BC7_SRGB_BLOCK, width: 16, height: 16, levels, srgb: true, ...BC7 }),
        );
        const texture = await loadChecked(device, path);
        // The colour space comes from the file — there is no caller-side option
        // to contradict it, unlike loadImageTexture's `colorSpace`.
        expect(texture.format).toBe("bc7-rgba-unorm-srgb");
    });

    it("loads BC5 (the normal-map format) and BC1 (the 4bpp format)", async () => {
        if (!device) return;
        const bc5 = write(
            "bc5.ktx2",
            buildKtx2({
                vkFormat: VK.BC5_UNORM_BLOCK,
                width: 32,
                height: 32,
                levels: mipChain(32, 32, BC5.blockBytes, BC5.blockW, BC5.blockH),
                ...BC5,
            }),
        );
        expect((await loadChecked(device, bc5)).format).toBe("bc5-rg-unorm");

        // BC1 has 8-byte blocks rather than 16 — if block size were hardcoded
        // anywhere, this is what would catch it.
        const bc1 = write(
            "bc1.ktx2",
            buildKtx2({
                vkFormat: VK.BC1_RGBA_UNORM_BLOCK,
                width: 32,
                height: 32,
                levels: mipChain(32, 32, BC1.blockBytes, BC1.blockW, BC1.blockH),
                ...BC1,
            }),
        );
        expect((await loadChecked(device, bc1)).format).toBe("bc1-rgba-unorm");
    });

    it("handles a non-square texture whose mip tail is smaller than one block", async () => {
        if (!device) return;
        // 12x4 is deliberately awkward while still legal: non-square, and the
        // chain runs 12x4, 6x2, 3x1, 1x1 — so every level after the first is
        // smaller than a 4x4 block in at least one axis and has to be uploaded
        // at its *physical* (block-rounded) extent rather than its logical size.
        // Getting that wrong fails only on these tail levels, which is why this
        // case exists separately from the square ones.
        const levels = mipChain(12, 4, BC7.blockBytes, BC7.blockW, BC7.blockH);
        expect(levels.length).toBe(4);
        expect((levels[0] as Uint8Array).length).toBe(3 * 1 * 16); // ceil(12/4)=3, ceil(4/4)=1
        expect((levels[3] as Uint8Array).length).toBe(16); // 1x1 still occupies a whole block

        const path = write("odd.ktx2", buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 12, height: 4, levels, ...BC7 }));
        const texture = await loadChecked(device, path);
        expect(texture.width).toBe(12);
        expect(texture.height).toBe(4);
        expect(texture.mipLevelCount).toBe(4);
    });

    it("transparently decompresses zstd supercompression", async () => {
        if (!device) return;
        const levels = mipChain(32, 32, BC7.blockBytes, BC7.blockW, BC7.blockH);
        const plain = buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 32, height: 32, levels, ...BC7 });
        const zstd = buildKtx2({
            vkFormat: VK.BC7_UNORM_BLOCK,
            width: 32,
            height: 32,
            levels,
            supercompression: SUPERCOMPRESSION.ZSTD,
            ...BC7,
        });
        // Sanity-check the fixture itself actually compressed — the counter
        // payload is highly compressible, so a same-size file would mean the
        // zstd branch of the writer silently did nothing.
        expect(zstd.length).toBeLessThan(plain.length);

        const texture = await loadChecked(device, write("zstd.ktx2", zstd));
        expect(texture.width).toBe(32);
        expect(texture.format).toBe("bc7-rgba-unorm");
        expect(texture.mipLevelCount).toBe(6);
    });

    it("accepts a single-level file with no mip chain", async () => {
        if (!device) return;
        const levels = mipChain(8, 8, BC7.blockBytes, BC7.blockW, BC7.blockH, 1);
        const path = write("single.ktx2", buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 8, height: 8, levels, ...BC7 }));
        expect((await loadChecked(device, path)).mipLevelCount).toBe(1);
    });
});

describe("loadKtx2Texture rejections", () => {
    // Every case here must reject the promise. The bar is not just "fails" but
    // "fails as a catchable JS error": a panic in the Rust worker would abort
    // the process instead, taking the test runner with it, so these double as
    // panic regression tests.

    it("rejects a truncated mip level rather than uploading garbage", async () => {
        if (!device) return;
        const levels = mipChain(32, 32, BC7.blockBytes, BC7.blockW, BC7.blockH);
        const short = levels[2] as Uint8Array;
        levels[2] = short.slice(0, short.length - 16); // one block short
        const path = write("truncated.ktx2", buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 32, height: 32, levels, ...BC7 }));
        expect(loadKtx2Texture(device, path)).rejects.toThrow(/truncated/i);
    });

    it("rejects a Basis Universal payload, naming the transcode requirement", async () => {
        if (!device) return;
        // vkFormat 0 (VK_FORMAT_UNDEFINED) is how KTX2 marks a universal payload.
        const levels = mipChain(16, 16, BC7.blockBytes, BC7.blockW, BC7.blockH, 1);
        const path = write(
            "basis.ktx2",
            buildKtx2({ vkFormat: VK.UNDEFINED, width: 16, height: 16, levels, supercompression: SUPERCOMPRESSION.BASIS_LZ, ...BC7 }),
        );
        expect(loadKtx2Texture(device, path)).rejects.toThrow(/transcode/i);
    });

    it("rejects ZLIB supercompression", async () => {
        if (!device) return;
        const levels = mipChain(16, 16, BC7.blockBytes, BC7.blockW, BC7.blockH, 1);
        const path = write(
            "zlib.ktx2",
            buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 16, height: 16, levels, supercompression: SUPERCOMPRESSION.ZLIB, ...BC7 }),
        );
        expect(loadKtx2Texture(device, path)).rejects.toThrow(/supercompression/i);
    });

    it("rejects cubemaps, texture arrays and 3D textures", async () => {
        if (!device) return;
        const levels = mipChain(16, 16, BC7.blockBytes, BC7.blockW, BC7.blockH, 1);
        const base = { vkFormat: VK.BC7_UNORM_BLOCK, width: 16, height: 16, levels, ...BC7 };

        const cube = write("cube.ktx2", buildKtx2({ ...base, faceCount: 6 }));
        expect(loadKtx2Texture(device, cube)).rejects.toThrow(/cubemap/i);

        const array = write("array.ktx2", buildKtx2({ ...base, layerCount: 4 }));
        expect(loadKtx2Texture(device, array)).rejects.toThrow(/array/i);

        const volume = write("3d.ktx2", buildKtx2({ ...base, pixelDepth: 4 }));
        expect(loadKtx2Texture(device, volume)).rejects.toThrow(/3D/i);
    });

    it("rejects base dimensions that are not a multiple of the block size", async () => {
        if (!device) return;
        // WebGPU will not create a block-compressed texture whose base extent is
        // unaligned, so this has to be caught rather than worked around — there
        // is no pixel data to invent for the partial block. Encoders pad; an
        // unaligned file is a bad asset.
        const levels = mipChain(13, 7, BC7.blockBytes, BC7.blockW, BC7.blockH, 1);
        const path = write("unaligned.ktx2", buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 13, height: 7, levels, ...BC7 }));
        expect(loadKtx2Texture(device, path)).rejects.toThrow(/not a multiple of/i);
    });

    it("rejects a format it does not map, pointing at the BC family", async () => {
        if (!device) return;
        const levels = mipChain(16, 16, BC7.blockBytes, BC7.blockW, BC7.blockH, 1);
        const path = write(
            "astc.ktx2",
            buildKtx2({ vkFormat: VK.ASTC_4x4_UNORM_BLOCK, width: 16, height: 16, levels, ...BC7 }),
        );
        expect(loadKtx2Texture(device, path)).rejects.toThrow(/BC/);
    });

    it("rejects a file that is not KTX2 at all", async () => {
        if (!device) return;
        const path = write("nope.ktx2", new Uint8Array([1, 2, 3, 4, 5, 6, 7, 8]));
        expect(loadKtx2Texture(device, path)).rejects.toThrow(/not a valid KTX2/i);
    });

    it("rejects a missing file", async () => {
        if (!device) return;
        expect(loadKtx2Texture(device, join(dir, "does-not-exist.ktx2"))).rejects.toThrow(/failed to read/i);
    });

    it("rejects with an actionable error when the device lacks texture-compression-bc", async () => {
        if (!plainDevice) return;
        // The whole point of the feature gate: there is no software fallback for
        // block-compressed data, so this must be a clear error rather than a
        // broken handle. `plainDevice` was created without requiredFeatures.
        expect(plainDevice.features.has("texture-compression-bc")).toBe(false);

        const levels = mipChain(16, 16, BC7.blockBytes, BC7.blockW, BC7.blockH, 1);
        const path = write("gate.ktx2", buildKtx2({ vkFormat: VK.BC7_UNORM_BLOCK, width: 16, height: 16, levels, ...BC7 }));
        expect(loadKtx2Texture(plainDevice, path)).rejects.toThrow(/texture-compression-bc/);
    });
});

// ── Real KTX-Software fixtures, with pixel assertions ───────────────────────
//
// Everything above asserts on structure: dimensions, format, mip count, and an
// empty validation error scope. None of it looks at a single texel, so none of
// it can catch the loader putting the right number of correct-sized bytes in the
// *wrong place* — mip levels swapped, or a level offset by a block. These tests
// close that by sampling the texture through a render pass (`readTexturePixels`
// rejects BC formats by design) and checking the colours that come back.
//
// The assertions are **exact**, not approximate. BC is lossy in general, but a
// 4x4 block of one uniform colour is something BC7/BC5/BC1 all represent
// exactly, so the fixture's block-aligned solid quadrants survive the round trip
// bit-for-bit — measured, not assumed. Exactness is what makes these strong: a
// tolerance wide enough to absorb real encoder error would also absorb a
// half-block offset.

const ASSETS = join(import.meta.dir, "assets");

describe("GPU-side validation errors reach the caller", () => {
    // The regression these guard against is specific and was live for exactly
    // one commit: wgpu 30's error scopes are thread-local, so a scope opened on
    // the JS thread cannot see work done inside `AsyncTask::compute` on a libuv
    // worker. Everything below happens on that worker.
    //
    // Both tests pass a *well-formed* file. The failure is raised by wgpu at
    // `create_texture`, not by any of this crate's own CPU-side checks — which
    // is the point, because CPU-side rejections were never affected.
    //
    // Mutation check: remove `with_validation_scope` from `LoadKtx2Task` and
    // both of these fail, while every other test in this file still passes.

    it("rejects when wgpu refuses the texture, instead of returning a broken one", async () => {
        if (!device) return;
        // STORAGE_BINDING is not allowed on block-compressed formats. Nothing
        // on the CPU side checks usage bits, so this reaches wgpu.
        const load = loadKtx2Texture(device, join(ASSETS, "quad-bc7.ktx2"), {
            usage: GPUTextureUsage.TEXTURE_BINDING
                | GPUTextureUsage.COPY_DST
                | GPUTextureUsage.STORAGE_BINDING,
        });
        await expect(load).rejects.toThrow(/STORAGE_BINDING/);
    });

    it("names the failing operation in the rejection", async () => {
        if (!device) return;
        // A bare "Validation Error" gives no clue which of a frame's many async
        // loads produced it, so the operation and path are prefixed.
        const load = loadKtx2Texture(device, join(ASSETS, "quad-bc7.ktx2"), {
            usage: GPUTextureUsage.TEXTURE_BINDING
                | GPUTextureUsage.COPY_DST
                | GPUTextureUsage.STORAGE_BINDING,
        });
        await expect(load).rejects.toThrow(/loadKtx2Texture\('.*quad-bc7\.ktx2'\)/);
    });

    it("still resolves normally when the usage flags are legal", async () => {
        if (!device) return;
        const texture = await loadKtx2Texture(device, join(ASSETS, "quad-bc7.ktx2"), {
            usage: GPUTextureUsage.TEXTURE_BINDING | GPUTextureUsage.COPY_DST,
        });
        expect(texture.format).toBe("bc7-rgba-unorm");
        expect(texture.usage & GPUTextureUsage.STORAGE_BINDING).toBe(0);
    });
});


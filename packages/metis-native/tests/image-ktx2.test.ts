// ── KTX2 fixture writer ─────────────────────────────────────────────────────
//
// Layout (KTX2 spec §3):
//   0..80            header + index
//   80..80+24*N      level index, one 24-byte entry per mip, level 0 first
//   then             the DFD section
//   then             level data
//
// Level *data* is written smallest-mip-first, which is what the `ktx` tool
// emits (it streams the small levels early). The index still lists level 0
// first. That mismatch is deliberate: a loader that walked the file in storage
// order instead of following the index would read the mip chain upside down and
// this fixture would catch it.

/** VkFormat values, matching `ktx2::Format`. */
const VK = {
    UNDEFINED: 0,
    BC1_RGBA_UNORM_BLOCK: 133,
    BC5_UNORM_BLOCK: 141,
    BC7_UNORM_BLOCK: 145,
    BC7_SRGB_BLOCK: 146,
    ASTC_4x4_UNORM_BLOCK: 157,
} as const;

const SUPERCOMPRESSION = { NONE: 0, BASIS_LZ: 1, ZSTD: 2, ZLIB: 3 } as const;

const KTX2_MAGIC = [0xab, 0x4b, 0x54, 0x58, 0x20, 0x32, 0x30, 0xbb, 0x0d, 0x0a, 0x1a, 0x0a];

/**
 * A minimal but structurally valid Basic Data Format Descriptor.
 *
 * The loader never reads the DFD — it takes the format from the header's
 * `vkFormat` — but `ktx2::Reader::new` parses and validates the section, so a
 * file without one is rejected before the loader sees it. Hence a real block
 * rather than zero padding: 8-byte block header, 16 fixed bytes, one 16-byte
 * sample.
 */
function basicDfd(blockBytes: number, blockW: number, blockH: number, srgb: boolean): Uint8Array {
    const SAMPLES = 1;
    const size = 8 + 16 + 16 * SAMPLES; // 40
    const dfd = new Uint8Array(size);
    const view = new DataView(dfd.buffer);

    // Block header: vendorId 0 | descriptorType 0, version 2, size.
    view.setUint32(0, 0, true);
    view.setUint16(4, 2, true);
    view.setUint16(6, size, true);

    dfd[8] = 0; // colorModel (unspecified — not read by the loader)
    dfd[9] = 1; // colorPrimaries: BT709
    dfd[10] = srgb ? 2 : 1; // transferFunction: 2 = sRGB, 1 = linear
    dfd[11] = 0; // flags
    // texelBlockDimension is stored as (dimension - 1).
    dfd[12] = blockW - 1;
    dfd[13] = blockH - 1;
    dfd[14] = 0;
    dfd[15] = 0;
    dfd[16] = blockBytes; // bytesPlane0
    return dfd;
}

interface Ktx2Spec {
    vkFormat: number;
    width: number;
    height: number;
    /** Raw (uncompressed) bytes for each mip, level 0 first. */
    levels: Uint8Array[];
    blockBytes: number;
    blockW: number;
    blockH: number;
    srgb?: boolean;
    supercompression?: number;
    layerCount?: number;
    faceCount?: number;
    pixelDepth?: number;
}

function buildKtx2(spec: Ktx2Spec): Uint8Array {
    const scheme = spec.supercompression ?? SUPERCOMPRESSION.NONE;
    const levelCount = spec.levels.length;

    // Supercompressed levels are stored compressed; `uncompressedByteLength`
    // still reports the original size.
    const stored = spec.levels.map((raw) =>
        scheme === SUPERCOMPRESSION.ZSTD ? new Uint8Array(zstdCompressSync(raw)) : raw,
    );

    const dfd = basicDfd(spec.blockBytes, spec.blockW, spec.blockH, spec.srgb ?? false);
    const levelIndexBytes = levelCount * 24;
    const dfdOffset = 80 + levelIndexBytes;
    const dfdByteLength = 4 + dfd.length; // 4-byte total-length field, then the block
    let cursor = dfdOffset + dfdByteLength;

    // Assign data offsets smallest-mip-first (see the note above).
    const offsets = new Array<number>(levelCount).fill(0);
    for (let i = levelCount - 1; i >= 0; i--) {
        offsets[i] = cursor;
        cursor += (stored[i] as Uint8Array).length;
    }

    const out = new Uint8Array(cursor);
    const view = new DataView(out.buffer);
    out.set(KTX2_MAGIC, 0);

    view.setUint32(12, spec.vkFormat, true);
    view.setUint32(16, 1, true); // typeSize: must be 1 for block-compressed formats
    view.setUint32(20, spec.width, true);
    view.setUint32(24, spec.height, true);
    view.setUint32(28, spec.pixelDepth ?? 0, true);
    view.setUint32(32, spec.layerCount ?? 0, true);
    view.setUint32(36, spec.faceCount ?? 1, true);
    view.setUint32(40, levelCount, true);
    view.setUint32(44, scheme, true);

    // Index: dfd offset/length, kvd offset/length (u32), sgd offset/length (u64).
    view.setUint32(48, dfdOffset, true);
    view.setUint32(52, dfdByteLength, true);
    view.setUint32(56, 0, true); // no key/value data
    view.setUint32(60, 0, true);
    view.setBigUint64(64, 0n, true); // no supercompression global data
    view.setBigUint64(72, 0n, true);

    for (let i = 0; i < levelCount; i++) {
        const base = 80 + i * 24;
        view.setBigUint64(base, BigInt(offsets[i] as number), true);
        view.setBigUint64(base + 8, BigInt((stored[i] as Uint8Array).length), true);
        view.setBigUint64(base + 16, BigInt((spec.levels[i] as Uint8Array).length), true);
    }

    view.setUint32(dfdOffset, dfdByteLength, true); // DFD total length field
    out.set(dfd, dfdOffset + 4);
    for (let i = 0; i < levelCount; i++) out.set(stored[i] as Uint8Array, offsets[i] as number);
    return out;
}

const mipSize = (base: number, level: number) => Math.max(1, base >> level);

/** Number of mips in a full chain down to 1x1. */
function fullMipCount(w: number, h: number): number {
    return Math.floor(Math.log2(Math.max(w, h))) + 1;
}

/**
 * Correctly-sized (but not meaningfully encoded) block payloads for a full mip
 * chain. The bytes are a counter, not a real BC encoding — nothing here samples
 * the texture, so what matters is that the *size* of every level is exactly what
 * the block layout demands.
 */
function mipChain(w: number, h: number, blockBytes: number, blockW: number, blockH: number, count?: number): Uint8Array[] {
    const n = count ?? fullMipCount(w, h);
    const levels: Uint8Array[] = [];
    for (let i = 0; i < n; i++) {
        const lw = mipSize(w, i);
        const lh = mipSize(h, i);
        const blocks = Math.ceil(lw / blockW) * Math.ceil(lh / blockH);
        const data = new Uint8Array(blocks * blockBytes);
        for (let b = 0; b < data.length; b++) data[b] = (b + i) & 0xff;
        levels.push(data);
    }
    return levels;
}

const BC7 = { blockBytes: 16, blockW: 4, blockH: 4 };
const BC5 = { blockBytes: 16, blockW: 4, blockH: 4 };
const BC1 = { blockBytes: 8, blockW: 4, blockH: 4 };

// ── setup ───────────────────────────────────────────────────────────────────

let adapter: GpuAdapter | null = null;
let device: GpuDevice | null = null;
/** A device deliberately created *without* texture-compression-bc. */
let plainDevice: GpuDevice | null = null;

// Coverage for `loadKtx2Texture` — the block-compressed loader.
//
// **Two tiers of fixture, on purpose.**
//
// 1. The **inline writer** below builds KTX2 files byte by byte. It exists for
//    the edge and failure cases no real encoder would produce on request —
//    truncated levels, cubemaps, BasisLZ payloads, unaligned dimensions — and
//    it puts the exact bytes going in next to the values asserted coming out,
//    the same reasoning as the TGA/HDR fixtures in image-formats.test.ts.
// 2. The **`quad-*.ktx2` files** in `tests/assets/` are genuine KTX-Software
//    v4.4.2 output, checked in, regenerated by
//    `tests/assets/generate-ktx2-fixtures.ts`. They prove the inline writer's
//    assumptions match reality (DFD contents, level ordering as actually
//    emitted, mip padding), and because their contents are known they are what
//    the pixel assertions run against.
//
// **What each tier can and cannot catch.** The block math is the whole risk in
// this loader: `bytes_per_row` counts blocks, not pixels, so a BC7 row is
// `ceil(w/4) * 16` and not `w * 4`. Getting that wrong does *not* throw — wgpu
// reports it as a validation error, and this binding does not surface those as
// exceptions (see "Debugging WebGPU validation errors" in CLAUDE.md). So every
// successful-load test wraps the call in an error scope and asserts the scope
// came back empty. Without that, a wrong stride would still produce a texture
// handle with the right dimensions and the suite would be green.
//
// But an error scope only proves wgpu accepted the upload — not that the bytes
// landed in the right place. Nothing in tier 1 ever looks at a texel, so it
// cannot see mip levels swapped or a level offset by a block. That is what the
// tier-2 pixel tests at the bottom of this file are for.
//
// The `12x4` case in tier 1 is the most load-bearing of the structural ones:
// non-square, with a mip tail below the 4x4 block size, which is exactly where a
// `ceil` written as a truncating divide, or a logical extent used where the
// physical one is required, goes wrong.

    it("places BC7 blocks such that every quadrant samples to its exact source colour", async () => {
        if (!device) return;
        const renderer = createTextureRenderer(device, Q, Q);
        try {
            const texture = await loadChecked(device, join(ASSETS, "quad-bc7.ktx2"));
            expect(texture.format).toBe("bc7-rgba-unorm");
            expect(texture.mipLevelCount).toBe(7);

            const px = await renderer.sample(texture, 0);
            expect(pixelAt(px, Q, ...QUADRANT.topLeft)).toEqual([255, 0, 0]);
            expect(pixelAt(px, Q, ...QUADRANT.topRight)).toEqual([0, 255, 0]);
            expect(pixelAt(px, Q, ...QUADRANT.bottomLeft)).toEqual([0, 0, 255]);
            expect(pixelAt(px, Q, ...QUADRANT.bottomRight)).toEqual([255, 255, 255]);
        } finally {
            renderer.destroy();
        }
    });

    it("uploads the mip chain in the right order, not reversed", async () => {
        if (!device) return;
        // The decisive check. Level 0 is four distinct quadrants; level 6 is 1x1,
        // the average of all four (~mid grey). If the chain were uploaded
        // reversed — or off by one — these two observations swap, and both
        // assertions fail. Structural tests cannot see this at all: every level
        // is exactly the size the loader expects either way.
        const renderer = createTextureRenderer(device, Q, Q);
        try {
            const texture = await loadChecked(device, join(ASSETS, "quad-bc7.ktx2"));

            const base = await renderer.sample(texture, 0);
            const corners = [
                pixelAt(base, Q, ...QUADRANT.topLeft),
                pixelAt(base, Q, ...QUADRANT.topRight),
                pixelAt(base, Q, ...QUADRANT.bottomLeft),
                pixelAt(base, Q, ...QUADRANT.bottomRight),
            ];
            expect(new Set(corners.map((c) => c.join(","))).size).toBe(4); // all distinct

            // The 1x1 tail: uniform, and near the average of the four quadrants
            // ((255+0+0+255)/4 = 127.5 per channel). Tolerance here is real —
            // this is a resample, not a flat block.
            const tail = await renderer.sample(texture, texture.mipLevelCount - 1);
            const [r, g, b] = pixelAt(tail, Q, 32, 32);
            expect(r).toBeGreaterThan(96);
            expect(r).toBeLessThan(160);
            expect(g).toBeGreaterThan(96);
            expect(g).toBeLessThan(160);
            expect(b).toBeGreaterThan(96);
            expect(b).toBeLessThan(160);
            // Uniform across the whole 1x1 level, wherever it is sampled.
            expect(pixelAt(tail, Q, 4, 60)).toEqual([r, g, b]);
        } finally {
            renderer.destroy();
        }
    });

    it("decompresses zstd to bytes identical to the uncompressed file", async () => {
        if (!device) return;
        // Supercompression is lossless, so this is an exact-equality test across
        // every mip level: anything other than a perfect match is a zstd bug,
        // and comparing full buffers catches a partial decompression that a
        // handful of sample points would miss.
        const renderer = createTextureRenderer(device, Q, Q);
        try {
            const plain = await loadChecked(device, join(ASSETS, "quad-bc7.ktx2"));
            const zstd = await loadChecked(device, join(ASSETS, "quad-bc7-zstd.ktx2"));
            expect(zstd.mipLevelCount).toBe(plain.mipLevelCount);

            for (let level = 0; level < plain.mipLevelCount; level++) {
                const a = await renderer.sample(plain, level);
                const b = await renderer.sample(zstd, level);
                expect(Buffer.from(b)).toEqual(Buffer.from(a));
            }
        } finally {
            renderer.destroy();
        }
    });

    it("loads real BC5 and BC1 files and samples them correctly", async () => {
        if (!device) return;
        const renderer = createTextureRenderer(device, Q, Q);
        try {
            // BC5 is two-channel — the format normal maps ship in, where the
            // shader reconstructs Z from X and Y. B always reads 0.
            //
            // The channel packing is worth stating because it is not the obvious
            // one: `ktx transcode --target bc5` maps **R -> R and A -> G**, the
            // normal-map convention, *not* R -> R and G -> G. The fixture is
            // opaque, so G reads 255 in every quadrant and only R carries
            // position here. That still distinguishes the red/white quadrants
            // from the green/blue ones; the full four-way positional proof is
            // the BC7 test above, which is the one that needs it.
            const bc5 = await loadChecked(device, join(ASSETS, "quad-bc5.ktx2"));
            expect(bc5.format).toBe("bc5-rg-unorm");
            const p5 = await renderer.sample(bc5, 0);
            expect(pixelAt(p5, Q, ...QUADRANT.topLeft)).toEqual([255, 255, 0]); // red   -> R 255
            expect(pixelAt(p5, Q, ...QUADRANT.topRight)).toEqual([0, 255, 0]); //  green -> R 0
            expect(pixelAt(p5, Q, ...QUADRANT.bottomLeft)).toEqual([0, 255, 0]); // blue -> R 0
            expect(pixelAt(p5, Q, ...QUADRANT.bottomRight)).toEqual([255, 255, 0]); // white -> R 255

            // The fixture is BC1_RGB_UNORM_BLOCK — the RGB spelling, which wgpu
            // has no separate format for, so it maps onto the RGBA one. These
            // primaries are exact under 565 quantisation.
            const bc1 = await loadChecked(device, join(ASSETS, "quad-bc1.ktx2"));
            expect(bc1.format).toBe("bc1-rgba-unorm");
            const p1 = await renderer.sample(bc1, 0);
            expect(pixelAt(p1, Q, ...QUADRANT.topLeft)).toEqual([255, 0, 0]);
            expect(pixelAt(p1, Q, ...QUADRANT.topRight)).toEqual([0, 255, 0]);
            expect(pixelAt(p1, Q, ...QUADRANT.bottomLeft)).toEqual([0, 0, 255]);
            expect(pixelAt(p1, Q, ...QUADRANT.bottomRight)).toEqual([255, 255, 255]);
        } finally {
            renderer.destroy();
        }
    });

    it("produces a sampleable texture with the default usage flags", async () => {
        if (!device) return;
        // The default is TEXTURE_BINDING | COPY_DST, so a caller that passes no
        // options gets something bindable — worth pinning, since every test
        // above would still pass if the default were wrong and the tests were
        // the only thing setting usage explicitly.
        const texture = await loadKtx2Texture(device, join(ASSETS, "quad-bc7.ktx2"));
        expect(texture.usage & GPUTextureUsage.TEXTURE_BINDING).toBeTruthy();
        expect(SAMPLEABLE & GPUTextureUsage.TEXTURE_BINDING).toBeTruthy();
    });
